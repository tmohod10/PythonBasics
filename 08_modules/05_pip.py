# PIP helps us to download the modules which are created
# by developers other than the Official Python developers
# and these modules helps us to improve and/or enhance
# our code by using their methods

# To install a python package index
# on PyCharm IDE, go to File > Settings > Project:{Project Name}
# > Python Interpreter

# or on terminal, install the pip3 package for the linux distro
# then use this command to install any package
# pip3 install <package_name>

# to remove a package
# pip3 remove <package_name>

# to install a package of a specific version
# first remove current version of the package then
# pip3 install <package_name>==<version_number>
