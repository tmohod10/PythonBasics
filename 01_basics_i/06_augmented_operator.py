# Augmented assignment operator
# (also known as shorthand operator in other languages)
# is a way to perform some operation on a variable which is already
# storing some value

# NOTE : a variable needs to have a value assigned to it
# then only we can use the augmented operator

count = 10
# one wat to update the existing value of count
# variable is to write like this
count = count + 2

# other way of writing the above code is using the augmented operator
count += 2

# similarly we can write
count -= 2
count *= 2
count /= 2
count //= 2
count **= 2
count %= 2