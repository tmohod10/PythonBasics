# To create the directory as a webserver. Run this command in the parent directory of the directory
# For Linux: python3 -m venv <directory>
# For Windows: py -3 -m venv <directory>
# 3 folders will be added in the directory:
# For Linux: Include, Lib and Scripts
# For Windows: Include, Lib and Bin

# Then we need to activate the environment
# For Linux: . <directory>/bin/activate
# For Windows: <directory>\Scripts\Activate

# Now we install Flask
# pip install Flask

